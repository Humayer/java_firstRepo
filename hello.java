public class hello {
    public static void main(String[] args) {
 // type casting       
        int a = 257;
        byte k = (byte) a;
        System.out.println(k);
        float f = 5.6f ;
        int t = (int) f ;
        System.out.println(t);
// type promotion
        byte c = 10 ;
        byte b = 30 ;
        int result = c * b ;
        System.out.println(result);
    }
    
}
